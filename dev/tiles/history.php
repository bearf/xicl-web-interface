<a href="#" id="about" class="anchor"></a>
<p>Открытый турнир имеет статус республиканского (регионального) и проводится Министерством образования и науки Республики Татарстан, ОАО «АйСиЭл&mdash;КПО ВС» при содействии Казанского IT-парка, Казанского (Приволжского) федерального университета, Казанского национального исследовательского технического университета имени А. Н. Туполева.</p>
<p>Основные цели и задачи турнира - создание оптимальных условий для выявления талантливых молодых программистов, их дальнейшего интеллектуального развития, адресная материальная поддержка школьников и студентов, занимающих призовые места в соревнованиях по программированию, реклама задействованных организаций и предприятий, а также учреждений образования, профориентация школьников, популяризация компьютерного образования, активизация работы спецкурсов, кружков, научных обществ студентов и учащихся, развитие других форм работы с одаренными детьми.</p> 
<p>В турнире принимают участие команды студентов вузов и учащихся средних школ, лицеев, гимназий Республики Татарстан, а также других регионов России. Личный зачет не подводится. 
<p>Команда:</p>
<ul><li>состоит 3-х человек;</li>
<li>должна быть составлена из учащихся одного лицея, школы, гимназии (для школьников);</li>
<li>должна быть составлена из учащихся одного ВУЗа (для студентов).</li></ul>
<p>По согласованию с оргкомитетом к соревнованиям допускаются сборные команды, составленные из учащихся нескольких учреждений образования.</p> 
<p>Турнир проводится ежегодно в I-м полугодии в городе Казани.</p>
<p>Турнир проводится в три тура: заочный отборочный тур, проводимый через Интернет, и очные туры (тренировочный и основной). Команды-победители заочного отборочного тура, а также команды, получившие специальные приглашения от организаторов, допускаются оргкомитетом турнира к участию в очных турах. В очных турах каждой команде предоставляется персональный компьютер и предлагается решить 6-12 задач. Продолжительность тренировочного тура - 3-4 часа, основного - 5 часов. Жюри может продлить время туров (отборочного, тренировочного и основного) в случае необходимости.</p> 
<p>Победителем турнира становится команда, решившая наибольшее количество задач в основном туре (результаты тренировочного тура в общий зачет не идут). Если несколько команд решили одинаковое количество задач, победитель определяется по штрафному времени.</p> 
<p>Турнир проводится на средства ОАО «АйСиЭл&mdash;КПО ВС». Участники турнира обеспечиваются бесплатным питанием. Иногородние участники обеспечиваются бесплатным проживанием. Команды, занявшие призовые места, награждаются материальными или денежными призами.</p>
<p>Окончательные итоги подводятся в последний день турнира. Команды школьников и студентов награждаются в разных зачетах. Победители студенческого и школьного зачета турнира награждаются дипломами I, II, III степени и призами. За успешное выступление участники турнира награждаются почетными грамотами. Все участники турнира получают сертификаты.</p>
<p>В соответствии с Постановлением №15 от 27.05.2010 Кабинета министров Республики Татарстан лучшая команда школьников Республики Татарстан и лучшая команда студентов Республики Татарстан, а также руководители данных команд получают премии Президента Республики Татарстан.</p>
