<h3>О Турнире</h3>
<p>Турнир ICL – это открытый чемпионат Татарстана по спортивному программированию среди школьников и студентов,  проводимый Министерством образования и науки Республики Татарстан, ОАО «АйСиЭл&mdash;КПО ВС» при содействии Казанского IT-парка, Казанского (Приволжского) федерального университета, Казанского национального исследовательского технического университета имени А. Н. Туполева. </p>

<p>Правила проведения основного тура близки к правилам командного чемпионата мира по программированию среди студентов ACM. За пять часов команде из трех человек предлагается решить набор задач на одном из разрешенных на турнире языков программирования. Успешное решение задач помимо знания собственно языков программирования предполагает математическую подготовку, знание алгоритмов и структур данных, а также слаженную командную работу в условиях дефицита времени. Проверка решений производится автоматической тестирующей системой.</p>

